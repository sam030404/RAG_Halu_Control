def get_chunks():
    """각 문서를 '단계' 단위로 잘라 청크 리스트로 반환.

    각 청크에는 소속 문서 제목을 접두로 붙여 검색 맥락을 보존한다.
    반환: [{"doc_id", "title", "text"}, ...]
    """
    chunks = []
    for doc in DOCUMENTS:
        for line in doc["content"].split("\n"):
            line = line.strip()
            if not line:
                continue
            chunks.append(
                {
                    "doc_id": doc["id"],
                    "title": doc["title"],
                    "text": f"[{doc['title']}] {line}",
                }
            )
    return chunks


if __name__ == "__main__":
    chunks = get_chunks()
    print(f"문서 {len(DOCUMENTS)}개, 청크 {len(chunks)}개\n")
    for doc in DOCUMENTS:
        n = sum(1 for c in chunks if c["doc_id"] == doc["id"])
        print(f"- {doc['title']}: {n}단계")
    print("\n[청크 예시]")
    for c in chunks[:2]:
        print(" ", c["text"])
